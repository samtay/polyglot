/// Given a triangle array, return the minimum path sum from top to bottom.

/// For each step, you may move to an adjacent number of the row below. More
/// formally, if you are on index i on the current row, you may move to either
/// index i or index i + 1 on the next row.
use std::cmp;

pub struct Solution();
impl Solution {
    /// Notice the overlapping structure and leverage Dynamic Programming.
    /// The minimum total for a given entry T[r, c] is
    ///     T[r, c] + min(T[r - 1, c], T[r - 1, c + 1])
    pub fn minimum_total(triangle: Vec<Vec<i32>>) -> i32 {
        // The minimum totals of the single element triangles in the bottom row are trivial
        let mut triangle = triangle;
        let mut min_tots = triangle.pop().unwrap(); // safe given constraints

        // Then, go up the triangle, and make the next row of minimum totals
        triangle.reverse();
        for row in triangle {
            let mut temp = Vec::with_capacity(row.len());
            for (col_ix, col) in row.into_iter().enumerate() {
                temp.push(col + cmp::min(min_tots[col_ix], min_tots[col_ix + 1]));
            }
            min_tots = temp;
        }

        // The final, single element row is the actual minimum total
        *min_tots.last().unwrap() // safe given constraints
    }

    /// Runs in O(2^n) time and O(2^n) space.
    pub fn minimum_total_bad(triangle: Vec<Vec<i32>>) -> i32 {
        #[derive(Debug, Clone, PartialEq)]
        pub struct Candidate {
            pub running_total: i32,
            pub col_index: usize,
        }
        // Since the array is not sorted, we'll have to do an exhaustive search
        // Although a sorted one would have a very trivial solution :)
        // Each step has two choices:
        let height = triangle.len();
        let total_candidates = 2 ^ (height - 1);

        let mut candidates: Vec<Candidate> = Vec::with_capacity(total_candidates);
        let mut triangle_iter = triangle.iter();
        // Guaranteed 1 <= triangle.length
        let top_vertex = triangle_iter.next().unwrap().into_iter().next().unwrap();
        candidates.push(Candidate {
            running_total: *top_vertex,
            col_index: 0,
        });

        // Starting from second row
        for row in triangle_iter {
            // Each current path leads to two new potential paths
            candidates = candidates
                .into_iter()
                .flat_map(|c| {
                    [
                        Candidate {
                            running_total: c.running_total + row[c.col_index],
                            col_index: c.col_index,
                        },
                        Candidate {
                            running_total: c.running_total + row[c.col_index + 1],
                            col_index: c.col_index + 1,
                        },
                    ]
                })
                .collect();
        }
        // safe unwrap due to constraints
        candidates
            .into_iter()
            .map(|c| c.running_total)
            .min()
            .unwrap()
    }
}

