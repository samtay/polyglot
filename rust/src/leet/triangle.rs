#[derive(Debug, Clone, PartialEq)]
pub struct Candidate {
    pub running_total: i32,
    pub col_index: usize,
}
pub struct Solution();
impl Solution {
    /// Runs in O(2^n) time and O(2^n) space
    ///
    /// To do this in O(n) space, the approach would change from going through
    /// all paths, and then taking a minimum across all of them, to going
    /// through paths one at a time, keeping track of a single minimum. Then the
    /// only space allocation that is necessary is a single path.
    pub fn minimum_total(triangle: Vec<Vec<i32>>) -> i32 {
        // Since the array is not sorted, we'll have to do an exhaustive search
        // Although a sorted one would have a very trivial solution :)
        // Each step has two choices:
        let height = triangle.len();
        let total_candidates = 2 ^ (height - 1);

        // Guaranteed 1 <= triangle.length
        let mut candidates: Vec<Candidate> = Vec::with_capacity(total_candidates);
        let mut triangle_iter = triangle.iter();
        let top_vertex = triangle_iter.next().unwrap().into_iter().next().unwrap();
        candidates.push(Candidate {
            running_total: *top_vertex,
            col_index: 0,
        });

        // Starting from second row
        for row in triangle_iter {
            // Each current path leads to two new potential paths
            candidates = candidates
                .into_iter()
                .flat_map(|c| {
                    [
                        Candidate {
                            running_total: c.running_total + row[c.col_index],
                            col_index: c.col_index,
                        },
                        Candidate {
                            running_total: c.running_total + row[c.col_index + 1],
                            col_index: c.col_index + 1,
                        },
                    ]
                })
                .collect();
        }
        // safe unwrap due to constraints
        candidates
            .into_iter()
            .map(|c| c.running_total)
            .min()
            .unwrap()
    }
}

